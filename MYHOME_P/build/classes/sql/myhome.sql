DROP SEQUENCE MEMBER2_SEQ;
DROP SEQUENCE BOARD2_SEQ;

CREATE SEQUENCE MEMBER2_SEQ START WITH 1 INCREMENT BY 1 MAXVALUE 999999 NOCYCLE NOCACHE;
CREATE SEQUENCE BOARD2_SEQ  START WITH 1 INCREMENT BY 1 MAXVALUE 999999 NOCYCLE NOCACHE;


DROP TABLE BOARD2;
DROP TABLE MEMBER2;


CREATE TABLE MEMBER2 (
	MNO NUMBER PRIMARY KEY,
	MID VARCHAR2(30) NOT NULL UNIQUE,
	MPW VARCHAR2(30) NOT NULL,
	MNAME VARCHAR2(30) NOT NULL,
	MEMAIL VARCHAR2(200) NOT NULL UNIQUE,
	MPHONE VARCHAR2(20),
	MADDRESS VARCHAR2(200),
	MREGDATE DATE
);
CREATE TABLE BOARD2 (
	BNO NUMBER PRIMARY KEY,
	MID VARCHAR2(30) REFERENCES MEMBER2 (MID),
	BTITLE VARCHAR2(1000) NOT NULL,
	BCONTENT VARCHAR2(4000),
	BIP VARCHAR2(30),  
	BHIT NUMBER,  
	BLASTMODIFY DATE,  
	BREGDATE DATE,  
	BDELETE NUMBER, 
	BGROUP NUMBER,  
	BGROUPORD NUMBER, 
	BDEPTH NUMBER  
);

